    private void jBT_VoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBT_VoltarActionPerformed
        int result = JOptionPane.showConfirmDialog(this,
                "As outras cargas foram entregues corretamente?", null, JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {

            try {
                final List<Object> listaCarga = cargaDao.listarCargasDoVeiculo(idVeiculoSelecionado);
                cargaDao = new CargaDao();

                if (listaCarga != null && listaCarga.size() > 0) {

                    for (Object cargaAtual : listaCarga) {
                        Carga carga = (Carga) cargaAtual;

                        if (carga.getStatus().equals("Saiu para entrega")) {
                            cargaDao.updateStatusDaCarga("Entregue", carga.getIdCarga());
                        }
                    }

                    final List<Object> listaCargaAguardando = cargaDao.listarCargasPorStatus(cargaSelecionada.getIdPedido_Cli(), "Aguardando");
                    if (listaCargaAguardando == null || listaCargaAguardando.size() < 1) {

                        final List<Object> listaCargaSaiuParaEntrega = cargaDao.listarCargasPorStatus(cargaSelecionada.getIdPedido_Cli(), "Saiu para entrega");
                        if (listaCargaAguardando == null || listaCargaAguardando.size() < 1) {
                            PedidoDao pedidoDao = new PedidoDao();
                            pedidoDao.update(cargaSelecionada.getIdPedido_Cli(), "Entregue");
                        }

                    }
                    JOptionPane.showMessageDialog(this, "Todas as cargas foram entregues com sucesso!");
                }

            } catch (Exception ex) {
                Logger.getLogger(FormVeiculoCargas.class.getName()).log(Level.SEVERE, null, ex);
                ex.printStackTrace();
            }
            VeiculoDao veiculoDao = new VeiculoDao();
            try {
                veiculoDao.updateStatus("Aguardando", idVeiculoSelecionado);
            } catch (Exception ex) {
                Logger.getLogger(FormVeiculoCargas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        dispose();
        
        
    }//GEN-LAST:event_jBT_VoltarActionPerformed

