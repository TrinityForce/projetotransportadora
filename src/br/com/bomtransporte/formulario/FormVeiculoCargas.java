    private void jBT_CargaExtraviadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBT_CargaExtraviadaActionPerformed
        //verifica se o status da carga e nulo
        if (cargaSelecionada.getStatus() == null || cargaSelecionada.getStatus().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao acessar o veiculo!\n O status do veiculo esta em branco.", "Erro", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int result = JOptionPane.showConfirmDialog(this,
                "Tem certeza que esta carga foi extraviada?", null, JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            cargaDao = new CargaDao();
            try {
                cargaDao.updateStatusDaCarga("Carga extraviada", cargaSelecionada.getIdCarga());
                cargaDao.updateIdVeiculo(cargaSelecionada.getIdCarga());
                preencherTabelaVeiculoCarga();
            } catch (Exception ex) {
                Logger.getLogger(FormVeiculoCargas.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        preencherTabelaVeiculoCarga();
    }//GEN-LAST:event_jBT_CargaExtraviadaActionPerformed

